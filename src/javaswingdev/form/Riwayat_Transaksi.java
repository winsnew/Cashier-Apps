    private String getAdditionalDetailFromRiwayatTransaksi(String kodeTransaksi) {
    String cashFormatted = ""; 

    try {
        st = cn.createStatement();
        ResultSet resultSet = st.executeQuery("SELECT cash FROM riwayat_transaksi WHERE kode_transaksi = '" + kodeTransaksi + "'");

        if (resultSet.next()) {
            // Retrieve the cash value from the result set
            double cashValue = resultSet.getDouble("cash");
            
            // Format the cash value as currency
            cashFormatted = formatCurrency(cashValue);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error retrieving additional detail: " + e.getMessage());
    }

    return cashFormatted;
}
    private String getAdditionalMenuFromRiwayatTransaksi(String kodeTransaksi) {
    String menuDetail = ""; // Default value, adjust as needed

    try {
        st = cn.createStatement();
        ResultSet resultSet = st.executeQuery("SELECT nama_menu FROM riwayat_transaksi WHERE kode_transaksi = '" + kodeTransaksi + "'");

        if (resultSet.next()) {
            // Retrieve the cash value from the result set
            String namaMenu = resultSet.getString("nama_menu");
            
            // Format the cash value as currency
            menuDetail = namaMenu;
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error retrieving additional detail: " + e.getMessage());
    }

    return menuDetail;
}
    private String formatCurrency(double amount) {
    // Format the amount as currency in Indonesian Rupiah format
    Locale localeID = new Locale("id", "ID");
    NumberFormat currencyFormat = DecimalFormat.getCurrencyInstance(localeID);
    return currencyFormat.format(amount);
}
    
