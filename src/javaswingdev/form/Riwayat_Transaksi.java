    public void tampil(){
        try{
            Object[] baris = {"kode_transaksi", "total_bayar", "kembalian", "tanggal"};
            tabmodel = new DefaultTableModel(null, baris){
                  public boolean isCellEditable(int row, int column){
                return false;
                }
            };
            
            st = cn.createStatement();
            rs = st.executeQuery("select * from riwayat_transaksi");
            while (rs.next()){
                String kd = rs.getString("kode_transaksi");
                String ttl = rs.getString("total_bayar");
                String kbl = rs.getString("kembalian");
                java.util.Date tglDate = rs.getDate("tanggal_bayar");
                String tgl = new SimpleDateFormat("dd-MM-yyyy").format(tglDate);
                String[] row = {kd, ttl, kbl, tgl};
                tabmodel.addRow(row);
            }
            riwayattransaksi.setModel(tabmodel);
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
